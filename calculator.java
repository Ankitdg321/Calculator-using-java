package calculator;

public class Calculator {
	
	public int add(int a, int b) {
		int sum =a+b;
		return sum;
		
	}
	public int sub(int a, int b) {
		int sub =a-b;
		return sub;
		
	}
	public int mul(int a, int b) {
		int mul =a*b;
		return mul;
		
	}
	public float div(int a, int b) {
		float div =a/b;
		return div;
		
	}

	public static void main(String[] args) {
	
		Calculator calc = new Calculator();
		System.err.println(calc.add(8, 5));
		System.err.println(calc.sub(78, 52));
		System.err.println(calc.div(89, 9));
		System.err.println(calc.mul(78, 45));

	}

}
